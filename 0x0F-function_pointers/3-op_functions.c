#include "3-calc.h"

/**
 * op_add - adds numbers
 * @a: first param
 * @b: second param
 * Return: the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts numbers
 * @a: first param
 * @b: second param
 * Return: the result
 */

int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies numbers
 * @a: first param
 * @b: second param
 * Return: the result
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides numbers
 * @a: first param
 * @b: second param
 * Return: the result
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulates numbers
 * @a: first param
 * @b: second param
 * Return: the result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
